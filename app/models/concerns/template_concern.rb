module TemplateConcern
  extend ActiveSupport::Concern
  def set_request_content
    if Rails.env.development?
      self.title ||= "仕事に対するモチベーション"
      self.description ||= "最近、仕事に対するモチベーションが低下しています。やりがいを感じられず、将来への不安が募っています。今の仕事を続けるべきか、それとも新しい道を模索すべきか悩んでいます。同じ環境でのルーティンに疲れ、新しい挑戦を求めていますが、安定感も捨てがたいです。どのようにして自分に合った仕事を見つけ、人生をより充実させることができるでしょうか？仕事と生活のバランスを取りながら、将来の展望を見据えるためのステップは何でしょうか？\r\nご助言いただければ幸いです。"
    end
  end

  def set_delivery_content
